export function getProductById(id: string | undefined): Product | undefined {
	if (!id) return undefined;
	return products.find((p) => p.id === id);
}

export function searchProducts(
	keyword: string,
	storeId: string,
	departmentCode?: DepartmentCode | "all"
): Product[] {
	const trimmed = keyword.trim();
	let storeProducts = products.filter((p) => p.storeId === storeId);
	if (departmentCode && departmentCode !== "all") {
		storeProducts = storeProducts.filter((p) => p.departmentCode === departmentCode);
	}
	if (!trimmed) return storeProducts;
	if (/^\d+$/.test(trimmed)) {
		return storeProducts.filter((p) => p.janCode.includes(trimmed));
	}
	return storeProducts.filter((p) => p.name.includes(trimmed));
}

export function findProductByJanCode(
	janCode: string,
	storeId: string
): Product | undefined {
	return products.find((p) => p.janCode === janCode && p.storeId === storeId);
}
